package methods;

public class ColorCheck {
    public static void main(String[] args) {
        String color = "blue";

        if (color.equals("red")) {
            System.out.println("Red is a warm color!");
        } else if (color.equals("blue")) {
            System.out.println("Blue is a cool color!");
        } else {
            System.out.println("That is a nice color!");
        }
    }
}
