class Operator{
    public static void main(String[] args) {
       int a=30;
       int b=2;
       int sum=a+b;
       int sub=a-b;
       int mult=a*b;
       int div=a/b;
       int mod=a%b;
       System.out.println("addition "+sum);
       System.out.println("subtraction "+sub);
       System.out.println("multiplication "+mult);
       System.out.println("division "+div);
       System.out.println("modulus "+mod);
    }
}